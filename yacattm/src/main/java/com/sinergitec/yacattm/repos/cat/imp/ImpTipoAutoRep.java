	@Override
	public TipoAuto getTipoAuto(int iModo, String cQuery) {

		Connection conexion = null;
		TipoAuto tipoAuto = new TipoAuto();

		try {

			conexion = ConexionApp.iniConexion();
			ResultSetHolder tt_TipoAuto = new ResultSetHolder();

			BooleanHolder lhResultado = new BooleanHolder();
			StringHolder chTexto = new StringHolder();
			app app = new app(conexion);

			app.as_ctTipoAuto_Carga(iModo, cQuery, tt_TipoAuto, lhResultado, chTexto);

			ResultSet rs_TipoAuto = tt_TipoAuto.getResultSetValue();

			if (rs_TipoAuto.next()) {
				tipoAuto.setCompania(rs_TipoAuto.getString("cCveCia"));
				tipoAuto.setTipo(rs_TipoAuto.getString("cTipo"));
				tipoAuto.setActivo(rs_TipoAuto.getBoolean("lActivo"));
				tipoAuto.setRowid(rs_TipoAuto.getBytes("id"));
			}

			this.setResultado(lhResultado.getBooleanValue());
			this.setMensaje(chTexto.getStringValue());

			app._release();

		} catch (Open4GLException | IOException | SQLException e) {
			e.printStackTrace();

			this.setResultado(true);
			this.setMensaje("error" + " " + "Open4GLException | IOException e" + " "
					+ this.getClass().getEnclosingMethod().getName());
		} finally {
			try {
				ConexionApp.finConexion(conexion);

			} catch (Open4GLException | IOException e) {
				e.printStackTrace();
				this.setResultado(true);
				this.setMensaje("error" + " " + "Open4GLException | IOException e" + " "
						+ this.getClass().getEnclosingMethod().getName());
			}
		}

		return tipoAuto;

	}

	@Override
	public List<TipoAuto> listaTipoAuto(int iModo, String cQuery) {

		Connection conexion = null;
		ArrayList<TipoAuto> lista = new ArrayList<TipoAuto>();

		try {

			conexion = ConexionApp.iniConexion();
			ResultSetHolder tt_TipoAuto = new ResultSetHolder();

			BooleanHolder lhResultado = new BooleanHolder();
			StringHolder chTexto = new StringHolder();
			app app = new app(conexion);

			app.as_ctTipoAuto_Carga(iModo, cQuery, tt_TipoAuto, lhResultado, chTexto);

			ResultSet rs_TipoAuto = tt_TipoAuto.getResultSetValue();

			while (rs_TipoAuto.next()) {

				TipoAuto tipoAuto = new TipoAuto();
				tipoAuto.setCompania(rs_TipoAuto.getString("cCveCia"));
				tipoAuto.setTipo(rs_TipoAuto.getString("cTipo"));
				tipoAuto.setActivo(rs_TipoAuto.getBoolean("lActivo"));
				tipoAuto.setRowid(rs_TipoAuto.getBytes("id"));

				lista.add(tipoAuto);

			}

			this.setResultado(lhResultado.getBooleanValue());
			this.setMensaje(chTexto.getStringValue());

			app._release();

		} catch (Open4GLException | IOException | SQLException e) {

			e.printStackTrace();

			this.setResultado(true);
			this.setMensaje("error" + " " + "Open4GLException | IOException e" + " "
					+ this.getClass().getEnclosingMethod().getName());

		} finally {
			try {
				ConexionApp.finConexion(conexion);

			} catch (Open4GLException | IOException e) {
				e.printStackTrace();
				this.setResultado(true);
				this.setMensaje("error" + " " + "Open4GLException | IOException e" + " "
						+ this.getClass().getEnclosingMethod().getName());
			}
		}

		return lista;

	}

